    private void delete_idActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_idActionPerformed
        String idnumber = id_f.getText();
        try {
            DBconnection.init();
            Connection c = DBconnection.getConnection();

            PreparedStatement ps = c.prepareStatement("SELECT * FROM table1 WHERE Id_num = " + idnumber);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                DeleteData();
            } else {
                JOptionPane.showMessageDialog(this, "ID Dont exist", "Registry", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (SQLException ex) {
            Logger.getLogger(ShoclarshipGui.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_delete_idActionPerformed

