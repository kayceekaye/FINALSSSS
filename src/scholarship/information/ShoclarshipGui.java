    void DeleteData() {
        String idnumber = id_f.getText();

            try {
                DBconnection.init();
                Connection c = DBconnection.getConnection();
                PreparedStatement ps = c.prepareStatement("delete from table1 where Id_num = " + idnumber);
                ps.execute();
            } catch (SQLException ex) {
                Logger.getLogger(ShoclarshipGui.class.getName()).log(Level.SEVERE, null, ex);
            }
            LoadData();
            JOptionPane.showMessageDialog(this, "Deleted Successfully", "Registry", JOptionPane.INFORMATION_MESSAGE);
        

        id_f.setText("");

    }

    void SaveData() {

        try {
            DBconnection.init();
            Connection c = DBconnection.getConnection();
            PreparedStatement ps = c.prepareStatement("INSERT INTO table1(fname,mname,lname,suffix,sex,address,contact)VALUES('" + fname.getText() + "','" + mname.getText() + "','" + lname.getText() + "','" + suffix.getText() + "','" + sex.getText() + "','" + address.getText() + "','" + cnum.getText() + "')");
            ps.execute();
            JOptionPane.showMessageDialog(this, "Data Registered", "Registry", JOptionPane.INFORMATION_MESSAGE);
            LoadData();

            fname.setText("");
            lname.setText("");
            mname.setText("");
            suffix.setText("");
            sex.setText("");
            address.setText("");
            cnum.setText("");
            fname.requestFocus();

        } catch (SQLException ex) {
            Logger.getLogger(ShoclarshipGui.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    void LoadData() {
        try {
            DBconnection.init();
            Connection c = DBconnection.getConnection();
            PreparedStatement ps = c.prepareStatement("SELECT * FROM table1");
            ResultSet rs = ps.executeQuery();
            Table.setRowCount(0);
            while (rs.next()) {
                Table.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7), rs.getString(8)});

            }

        } catch (SQLException ex) {
            Logger.getLogger(ShoclarshipGui.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

